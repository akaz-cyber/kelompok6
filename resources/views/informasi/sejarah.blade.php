                <p>
                Sejarah Fotografi dimulai pada abad ke-19. Tahun 1839 merupakan tahun awal
                kelahiran fotografi. Pada saat itu, di Perancis dinyatakan secara resmi bahwa fotografi adalah
                sebuah terobosan teknologi. Saat itu, rekaman dua dimensi seperti yang dilihat mata sudah bisa dibuat permanen.
                 </p>
                 <p>
                Sejarah fotografi bermula jauh sebelum Masehi. Pada abad ke-5 Sebelum Masehi (SM), seorang pria bernama Mo Ti mengamati
                suatu gejala. Jika pada dinding ruangan yang gelap terdapat lubang kecil (pinhole), maka di bagian dalam ruang itu akan
                terefleksikan pemandangan di luar ruang secara terbalik lewat lubang tadi. Mo Ti adalah orang pertama yang menyadari fenomena kamera obscura. 
                 </p>
                 <p>
                Berabad-abad kemudian, banyak yang menyadari dan mengagumi fenomena ini, sebut saja Aristoteles pada abad ke-3 SM dan seorang ilmuwan Arab Ibnu Al Haitam 
                (Al Hazen) pada abad ke-10 SM, yang berusaha untuk menciptakan serta mengembangkan alat yang sekarang dikenal sebagai kamera. Pada tahun 1558, seorang ilmuwan 
                Italia, Giambattista della Porta menyebut  ”camera obscura” pada sebuah kotak yang membantu pelukis menangkap bayangan gambar.
                 </p>
                 <p>
                Nama kamera obscura diciptakan oleh Johannes Kepler pada tahun 1611. Johannes Kepler 
                membuat desain kamera portable yang dibuat seperti sebuah tenda, dan memberi nama alat tersebut kamera obscura.
                Didalam tenda sangat gelap kecuali sedikit cahaya yang ditangkap oleh lensa, yang membentuk gambar keadaan di luar tenda di atas selembar kertas.
                 </p>
                 <h3>seniman pada abad-19 menggunakan kamera obscura untuk membuat sketsa yaitu gambar 3D kamera obscura</h3>
                 <img src="assets/img/blog/kamera obscura.jpg" class="img-fluid" alt="">
                 <p>
                Berbagai penelitian dilakukan mulai pada awal abad ke-17 ,seorang ilmuwan berkebangsaan Italia – Angelo Sala menggunakan
                cahaya matahari untuk merekam serangkaian kata pada pelat chloride perak. Tapi ia gagal mempertahankan gambar secara permanen. 
                Sekitar tahun 1800, Thomas Wedgwood, seorang berkebangsaan Inggris bereksperimen untuk merekam gambar positif dari citra pada
                kamera obscura berlensa, hasilnya sangat mengecewakan. Humphrey Davy melakukan percobaan lebih lanjut dengan chlorida perak, tapi
                bernasib sama juga walaupun sudah berhasil menangkap imaji melalui kamera obscura tanpa lensa.
                 </p>
                 <p>
                Akhirnya, pada tahun 1824, seorang seniman lithography Perancis, Joseph-Nicephore Niepce (1765-1833), setelah delapan
                jam meng-exposed pemandangan dari jendela kamarnya, melalui proses yang disebutnya Heliogravure (proses kerjanya mirip lithograph)
                di atas pelat logam yang dilapisi aspal, berhasil melahirkan sebuah gambar yang agak kabur, berhasil pula mempertahankan gambar
                secara permanen. Ia melanjutkan percobaannya hingga tahun 1826, inilah yang akhirnya menjadi sejarah awal fotografi yang sebenarnya.
                Foto yang dihasilkan itu kini disimpan di University of Texas di Austin, AS.
                 </p>
                 <h3>
                “View from the Window at Le Gras” foto pertama yang berhasil dicetak meskipun 
                masih tampak kabur, dibuat oleh Joseph Nicéphore Niépce
                 </h3>
                 <p>
                Penelitian demi penelitian terus berlanjut hingga pata tanggal tanggal 19 Agustus 1839, desainer panggung opera yang juga pelukis,
                Louis-Jacques Mande’ Daguerre (1787-1851) dinobatkan sebagai orang pertama yang berhasil membuat foto yang sebenarnya: sebuah gambar
                permanen pada lembaran plat tembaga perak yang dilapisi larutan iodin yang disinari selama satu setengah jam cahaya langsung dengan
                pemanas merkuri (neon). Proses ini disebut daguerreotype. Untuk membuat gambar permanen, pelat dicuci larutan garam dapur dan asir 
                suling. Januari 1839, Daguerre sebenarnya ingin mematenkan temuannya itu. Akan tetapi, Pemerintah Perancis berpikir bahwa temuan itu
                sebaiknya dibagikan ke seluruh dunia secara cuma-cuma.
                 </p>
                 <h3>
                 “Boulevard du Temple” foto pertama yang diakui secara umum, dibuat oleh Louis Daguerre
                 </h3>
                 <p>
                Fotografi kemudian berkembang dengan sangat cepat. Melalui perusahaan Kodak Eastman, George Eastman mengembangkan fotografi
                dengan menciptakan serta menjual roll film dan kamera boks yang praktis, sejalan dengan perkembangan dalam dunia fotografi melalui
                perbaikan lensa, shutter, film dan kertas foto.
                 </p>
                 <p>
                Tahun 1950, untuk memudahkan pembidikan pada kamera Single Lens Reflex maka mulailah digunakan prisma (SLR),
                dan Jepang pun mulai memasuki dunia fotografi dengan produksi kamera Nikon yang kemudian disusul dengan Canon. 
                Tahun 1972 kamera Polaroid temuan Edwin Land mulai dipasarkan. Kamera Polaroid mampu menghasilkan gambar tanpa melalui 
                proses pengembangan dan pencetakan film.
                 </p>
